package com.kumarp.productservice.service;

import com.kumarp.productservice.dto.ProductRequest;
import com.kumarp.productservice.dto.ProductResponse;
import com.kumarp.productservice.model.Product;
import com.kumarp.productservice.repository.ProductRepository;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.stereotype.Service;

import java.util.List;

@Service
@RequiredArgsConstructor
@Slf4j
public class ProductService {

    private final ProductRepository productRepository; // Here we are doing constructor based injection

    /*  Constructor Based object Injection
    public ProductService(ProductRepository productRepository) {
        this.productRepository = productRepository;
    }
    */

    public void createProduct(ProductRequest productRequest){
        Product product = Product.builder()
                .name(productRequest.getName())
                .description(productRequest.getDescription())
                .price(productRequest.getPrice())
                .build();
        productRepository.save(product);
        log.info("Product {} is saved",product.getId()); // Log using Slf4j from Lombok
    }

    public List<ProductResponse> getAllProducts() {
        List<Product> products = productRepository.findAll();
        return products.stream().map(this::mapToProductResponse).toList();
    }

    private ProductResponse mapToProductResponse(Product product) {
        return ProductResponse.builder()
                .id(product.getId())
                .name(product.getName())
                .description(product.getDescription())
                .price(product.getPrice())
                .build();
    }
}
